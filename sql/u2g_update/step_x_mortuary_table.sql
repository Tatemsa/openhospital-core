CREATE TABLE OH_CAUSE(
    CS_ID INT AUTO_INCREMENT,
    CS_CODE VARCHAR(50),
    CS_DESC VARCHAR(255),
    CS_CREATED_BY VARCHAR(50) NOT NULL,
    CS_CREATED_DATE datetime NULL DEFAULT NULL,
    CS_LAST_MODIFIED_BY VARCHAR(50) NOT NULL,
    CS_LAST_MODIFIED_DATE datetime NULL DEFAULT NULL,
    CS_ACTIVE TINYINT(1) NOT NULL DEFAULT 1,
    PRIMARY KEY (CS_ID)
)ENGINE = INNODB DEFAULT CHARACTER SET utf8;

CREATE TABLE OH_MORTUARY (
    MRT_ID INT AUTO_INCREMENT,
    MRT_PLACE VARCHAR(100),
    MRT_PAT_ID INT NOT NULL,
    MRT_CS_ID INT NOT NULL,
    MRT_PROVENANCE VARCHAR(100),
    MRT_DEATH_DATE DATETIME NOT NULL,
    MRT_ENTERED_DATE DATETIME NOT NULL,
    MRT_RELEASE_DATE DATETIME NOT NULL,
    MRT_PROVISIONAL_RELEASE_DATE DATETIME NOT NULL,
    MRT_DECLARING_NAME VARCHAR(100) NOT NULL,
   	MRT_DECLARING_PHONE VARCHAR(20),
   	MRT_DECLARING_NEST VARCHAR(20),
   	MRT_LOCKER VARCHAR(20),
    MRT_CREATED_BY VARCHAR(50) NOT NULL,
    MRT_FAMILY_NAME VARCHAR(100),
    MRT_FAMILY_PHONE VARCHAR(20),
    MRT_FAMILY_NEST VARCHAR(20),
    MRT_CREATED_DATE datetime NULL DEFAULT NULL,
    MRT_LAST_MODIFIED_BY VARCHAR(50) NOT NULL,
    MRT_LAST_MODIFIED_DATE datetime NULL DEFAULT NULL,
    MRT_ACTIVE TINYINT(1) NOT NULL DEFAULT 1,
    MRT_DELETED TINYINT(1) NOT NULL DEFAULT 0,
    PRIMARY KEY (MRT_ID),
    CONSTRAINT FK_MORTUARY_PAT_ID FOREIGN KEY (MRT_PAT_ID) REFERENCES OH_PATIENT (PAT_ID) ON DELETE NO ACTION ON UPDATE NO ACTION,
    CONSTRAINT FK_MORTUARY_CS_ID FOREIGN KEY (MRT_CS_ID) REFERENCES OH_CAUSE (CS_ID) ON DELETE NO ACTION ON UPDATE NO ACTION,
    CONSTRAINT FK_MORTUARY_CREATED_BY FOREIGN KEY (MRT_CREATED_BY) REFERENCES OH_USER (US_ID_A) ON DELETE NO ACTION ON UPDATE NO ACTION,
    CONSTRAINT FK_MORTUARY_LAST_MODIFIED_BY FOREIGN KEY (MRT_LAST_MODIFIED_BY) REFERENCES OH_USER (US_ID_A) ON DELETE NO ACTION ON UPDATE NO ACTION
)ENGINE = INNODB DEFAULT CHARACTER SET utf8;